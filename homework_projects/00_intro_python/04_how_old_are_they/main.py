# This is a simple program to calculate the ages of five friends based on given conditions.
# The ages are calculated and printed in a formatted string.

def main():
    # Define ages based on given conditions
    Anton = 21
    Beth = Anton + 6
    Chen = Beth + 20
    Drew = Chen + Anton
    Ethan = Chen

    # Print names and ages
    print(f"Anton is {Anton}")
    print(f"Beth is {Beth}")
    print(f"Chen is {Chen}")
    print(f"Drew is {Drew}")
    print(f"Ethan is {Ethan}")



# Python file to call the main() function.
if __name__ == '__main__':
    main()
